package com.ExpressMessaging1.ExpressMessaging1.Users;

public class Users {

    private String id;
    private String name;
    private String lastName;
    private String celPhone;
    private String email;
    private String address;
    private String city;

    public Users(String id, String name, String lastName, String celPhone, String email, String address, String city) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
        this.celPhone = celPhone;
        this.email = email;
        this.address = address;
        this.city = city;
    }
}
